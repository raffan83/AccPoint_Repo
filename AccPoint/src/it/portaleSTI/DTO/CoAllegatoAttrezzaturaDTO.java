package it.portaleSTI.DTO;

public class CoAllegatoAttrezzaturaDTO {
	
	private int id;
	private int id_attrezzatura;
	private String nome_file;
	private int disabilitato;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getId_attrezzatura() {
		return id_attrezzatura;
	}
	public void setId_attrezzatura(int id_attrezzatura) {
		this.id_attrezzatura = id_attrezzatura;
	}
	public String getNome_file() {
		return nome_file;
	}
	public void setNome_file(String nome_file) {
		this.nome_file = nome_file;
	}
	public int getDisabilitato() {
		return disabilitato;
	}
	public void setDisabilitato(int disabilitato) {
		this.disabilitato = disabilitato;
	}
	
	

}
