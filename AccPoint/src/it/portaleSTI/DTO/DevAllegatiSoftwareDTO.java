package it.portaleSTI.DTO;

public class DevAllegatiSoftwareDTO {

	private int id;
	private String nome_file;
	private int id_software;
	private int disabilitato;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome_file() {
		return nome_file;
	}
	public void setNome_file(String nome_file) {
		this.nome_file = nome_file;
	}
	public int getId_software() {
		return id_software;
	}
	public void setId_software(int id_software) {
		this.id_software = id_software;
	}
	public int getDisabilitato() {
		return disabilitato;
	}
	public void setDisabilitato(int disabilitato) {
		this.disabilitato = disabilitato;
	}

	
	
	
}
